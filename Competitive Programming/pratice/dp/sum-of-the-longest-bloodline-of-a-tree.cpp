class Solution
{
public:

    void find_sum_of_longest_path(Node *root, int curr_len, int curr_sum, int &max_len, int &max_sum) {
        if (root == NULL) {
            if (curr_len > max_len) {
                max_len = curr_len;
                max_sum = curr_sum;
            } else if (curr_len == max_len && curr_sum > max_sum) {
                max_sum = curr_sum;
            }
            return;
        }
        find_sum_of_longest_path(root->left, curr_len+1, curr_sum+root->data, max_len, max_sum);
        find_sum_of_longest_path(root->right, curr_len+1, curr_sum+root->data, max_len, max_sum);
    }

    int sumOfLongRootToLeafPath(Node *root)
    {
        if (root == NULL)
            return 0;
        int max_len = 0, max_sum = INT_MIN;
        find_sum_of_longest_path(root, 0, 0, max_len, max_sum);
        return max_sum;
    }

};

