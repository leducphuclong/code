# Import session
import sys
input = sys.stdin.readline
from collections import defaultdict

############ ---- Input Functions ---- ############
def inp():
    return int(input())

def inlt():
    return list(map(int, input().split()))

def instr():
    return list(input().strip())

def invr():
    return map(int, input().split())

############ ---- Other Functions ---- ############
# Precompute
    
# Main function
for _ in range(inp()):
    x, y = invr()
    x, y = abs(x), abs(y)
    ans = min(x*2, y*2)
    if x - y != 0:
        ans += abs(x-y)*2-1
    print(ans)