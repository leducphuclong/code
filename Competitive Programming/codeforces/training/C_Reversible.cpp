void solve() {
    lo n;   cin >> n;
    map<string, bool> mrk;
    set<string> res;
    while (n--) {
        string s;   cin >> s;
        if (mrk[s] == false)
            res.insert(s);
        string tmp = s;
        reverse(tmp.begin(), tmp.end());
        mrk[s] = true;
        mrk[tmp] = true;
    }

    cout << res.size() << nln;
}

