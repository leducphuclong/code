# Giới thiệu lệnh replace

# Ví dụ: thay thế các kí tự a trong xâu S thành các kí tự b
# Cách dùng: S.replace(a, b)
# Ví dụ: S = abcd ->(Sau khi thực hiên) S = bbcd

# Cách sử dụng khác: xóa kí tự a trong xâu S
# -> Tức là ta đang thay thế kí tự a bằng kí tự rỗng
# Cách dùng: S.replace(a, '')
# Ví dụ: S = abc -> S = bc

# Yêu cầu đề: Xóa các kí tự c -> Thay thế kí tự c trong S
# bởi rỗng
# -> Sử dụng lệnh replace

